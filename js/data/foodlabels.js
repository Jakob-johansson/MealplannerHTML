function translateFoodLabel(englishDescription) {
    const lower = englishDescription.toLowerCase();

    for (const entry of FOOD_LABELS) {
        const allMatch = entry.keywords.every(keyword => lower.includes(keyword));
        if (allMatch) return entry.label;
    }

    return englishDescription;
}
